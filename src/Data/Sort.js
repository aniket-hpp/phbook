//function to set Sorting way [names]
function compareName(a, b){
    const name1 = a.name.toUpperCase();
    const name2 = b.name.toUpperCase();

    let comparison = 0;

    if (name1 > name2) {
        comparison = 1;
    } else if (name1 < name2) {
        comparison = -1;
    }
    return comparison;
}

//function to Sort userData based on names
const Sort = (Data) => {
    return Data.sort((a, b) => compareName(a, b))
}

export default Sort